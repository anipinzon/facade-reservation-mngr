package co.edu.poli.model;

public class Vehicle {
	
	private int id;
	
	private String type;
	

	public Vehicle() {
		super();

	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
	

}
